    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        // TODO add your handling code here:         
        String url="jdbc:mysql://localhost:3306/ECS";
        String user="root";
        String pwd="";
        String query = "select * from ACTIVE_USER;";
        int i = jTable.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)jTable.getModel();
        String from, to, subject, body;
        from = model.getValueAt(i, 0).toString();
        subject = model.getValueAt(i, 1).toString();
        body = model.getValueAt(i, 2).toString();
        try {
            Connection conn = DriverManager.getConnection (url, user, pwd);
            Statement stm= conn.createStatement();
            ResultSet rs = stm.executeQuery(query);
            to="";
            if(rs.next()){
                to = rs.getString("USER_ID");
            }
            String checkquery="delete from CURRENT_MAIL;";
            int r = stm.executeUpdate(checkquery);
            String query1="insert into CURRENT_MAIL values (?, ?, ?, ?) ";
            PreparedStatement stmnt = conn.prepareCall(query1);
            stmnt.setString(1, from);
            stmnt.setString(2, to);
            stmnt.setString(3, subject);
            stmnt.setString(4, body);
            stmnt.execute();
            
            MessageBox msg = new MessageBox ();
            msg.check = 0;
            msg.setVisible(true); 
            this.dispose();
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        
    }//GEN-LAST:event_jTableMouseClicked

