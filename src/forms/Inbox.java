    private void checkMessages(){
        DefaultTableModel model= (DefaultTableModel)jTable.getModel();
        String url="jdbc:mysql://localhost:3306/ECS";
        String user="root";
        String pwd= "";
        String query= "select * from MAILS; "; 
        try{
            Connection conn = DriverManager.getConnection (url, user, pwd); 
            Statement st = conn.createStatement();
            Statement stmnt = conn.createStatement();
            ResultSet rs= st.executeQuery(query);
            String query1 = "select * from ACTIVE_USER;";
            ResultSet rs1 = stmnt.executeQuery(query1);
            String current_to="";
            if(rs1.next()){
                current_to = rs1.getString("USER_ID");
            }
            
            while (rs.next()){  
                String from = rs.getString("FRM");
                String to = rs.getString("TOO");
                String subject = rs.getString ("SUB"); 
                String body = rs.getString("BODY");
                if (current_to.equals(to))
                {
                    model.addRow(new Object[] {from, subject, body});
                }
            }
            rs.close();
            rs1.close();
            st.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
